package com.poll;




public class variables {

    private int yc;
    private int nc;
    private String name;


    public variables() {
    }
    public void setyc(int yc) {
        this.yc = yc;
    }
    public void setnc (int nc) {
        this.nc = nc;
    }
    public void setname(String name) {
        this.name = name;
    }
    public int getyc() {
        return yc;
    }
    public int getnc() {
        return nc;
    }
    public String  getname() {
        return name;
    }

}

